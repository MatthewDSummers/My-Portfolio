let currentSeriesKey;


export function setSeriesKey(seriesKey) {
    currentSeriesKey = seriesKey;
}


function getCharsInListOfImportance() {
    return REORDERED_CHAR_NAMES[currentSeriesKey] || [];
}

// Sort API results by char name, in the custom order of importance
export function sortFFNames(a, b) {
    const list_in_order_of_char_importance = getCharsInListOfImportance();

    return list_in_order_of_char_importance.indexOf(a.name) - list_in_order_of_char_importance.indexOf(b.name);
}

export function performLegalities(num_for_reference){
    const my_statement = document.getElementById("my-statement");
    const m_u_g_div = document.getElementById("material-usage-guidelines-div");

    // reset the usage policy div
    m_u_g_div.innerHTML = "";
    m_u_g_div.appendChild(my_statement);

    // get game specific policy
    let material_usage_policy = MATERIAL_USAGE_GUIDELINES[num_for_reference];

    if (material_usage_policy){
        let usage_policy_p = document.createElement("p");

        usage_policy_p.innerHTML = material_usage_policy;
        usage_policy_p.classList.add("usage-policy");

        m_u_g_div.appendChild(usage_policy_p);
    }
}